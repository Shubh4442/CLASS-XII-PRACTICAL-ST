-- Create Database
CREATE DATABASE general_store;
USE general_store;

-- Products Table
CREATE TABLE Products (
    product_id INT PRIMARY KEY,
    product_name VARCHAR(50),
    price FLOAT,
    quantity INT
);

-- Sales Table
CREATE TABLE Sales (
    bill_no INT PRIMARY KEY,
    customer_name  varchar(30),
    product_name varchar(80),
    quantity_sold INT,
    total_amount FLOAT
);

